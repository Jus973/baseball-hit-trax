

public class graphable {
    
    public double x;
    public double y;
    public double z;
    
    public graphable (double tX, double tY, double tZ){
        x = tX;
        y = tY;
        z = tZ;
    }
    
    public graphable (double tX, double tY){
        x = tX;
        y = tY;
        z = 0;
    }

}